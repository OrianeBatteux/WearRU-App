import Foundation

/**
Documentation de la structure DayHours.
Cette structure représente des horaires de journée.
*/

struct DayHours {
    ///Horaire d'ouverture du Matin
    var morningOpening : String
    ///Horaire de fermeture du Matin
    var morningClosing : String
    ///Horaire d'ouverture de l'après midi
    var afternoonOpening : String
    ///Horaire de fermeture de l'après midi
    var afternoonClosing : String
}
